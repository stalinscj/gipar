    private String construirSentencia(){
        
        String from,where;
        
        if(!chkNombre.isSelected() && !chkAutor.isSelected() && !chkArea.isSelected() && !chkProyecto.isSelected() && !chkEvento.isSelected())
            where="";
        else
            where="WHERE ";
        
        from = "articulos";
            
        if(chkNombre.isSelected())
            where = where + "articulos.nombre='"+txtNombre.getText()+"' ";
        
        if(chkArea.isSelected()){
            if(chkNombre.isSelected())
                where = where + "AND ";
        
            where = where + "articulos.area='"+txtArea.getText()+"' ";
        }
        
        if(chkAutor.isSelected()){
            if(chkNombre.isSelected() || chkArea.isSelected())
                where = where + "AND ";
            
            where = where + "miembros.nombre='"+txtAutor.getText()+"' ";
            from = from +" INNER JOIN (miembros INNER JOIN generaciones ON miembros.idMiembro = generaciones.idMiembro) ON articulos.idArticulo=generaciones.idArticulo";
        }
        
        if(chkProyecto.isSelected()){
            if(chkNombre.isSelected() || chkArea.isSelected() || chkAutor.isSelected())
                where = where + "AND ";
            
            where = where + "proyectos.nombre='"+txtProyecto.getText()+"' ";
            from = from +" INNER JOIN (proyectos INNER JOIN articulos_proyectos ON proyectos.idProyecto = articulos_proyectos.idProyecto) ON articulos.idArticulo=articulos_proyectos.idArticulo";
        }
        
        if(chkEvento.isSelected()){
            if(chkNombre.isSelected() || chkArea.isSelected() || chkAutor.isSelected() || chkProyecto.isSelected())
                where = where + "AND ";
            
            where = where + "eventos.nombre='"+txtEvento.getText()+"' ";
            from = from +" INNER JOIN (eventos INNER JOIN articulos_eventos ON eventos.idEvento = articulos_eventos.idEvento) ON articulos.idArticulo=articulos_eventos.idArticulo";
        }
        
        return "SELECT articulos.nombre,area,link FROM "+from+" "+where+";";
    }
    
