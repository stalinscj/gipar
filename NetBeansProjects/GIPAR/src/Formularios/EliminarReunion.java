    public boolean cargarReuniones(){
        
        String datos[][]={};
        String columnas[]={"Reunion","C.I. Coordinador","Nombre","Fecha","Hora"};
        modelo= new DefaultTableModel(datos,columnas){
                        @Override
                        public boolean isCellEditable(int row, int column) {
                            return false;
                        }
                    };
            tblReuniones.setModel(modelo);
        
        
        String usuario;
        if(this.usuarioActual.getCargo().equalsIgnoreCase("Miembro"))
            usuario="miembro";
        else
            usuario="miembrogipar";
        
        ConexionMYSQL mysql = new ConexionMYSQL(hostname,"bdgipar",usuario);
        Connection conexion = mysql.conectar();
        String sentenciaSQL;
        
        sentenciaSQL = "SELECT nombre,cedula,fecha,hora,idReunion "
                     
                     + "FROM miembros INNER JOIN reuniones ON miembros.idMiembro = reuniones.idMiembro ORDER BY fecha,hora;";
        
        int i=0;
        
        try {
            Statement ST = conexion.createStatement();
            ResultSet RS = ST.executeQuery(sentenciaSQL);
            
            while(RS.next()){
                modelo.insertRow(i, new Object[]{});
                modelo.setValueAt(RS.getString("idReunion"),i, 0);
                modelo.setValueAt(RS.getString("cedula"),i, 1);
                modelo.setValueAt(RS.getString("nombre"),i, 2);
                modelo.setValueAt(RS.getString("fecha"),i, 3);
                modelo.setValueAt(RS.getString("hora"),i, 4);
                cboReunion.addItem(RS.getString("idReunion"));
                i++;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        return true;
    }
    
    
    public boolean eliminarReunion(){
        
        String usuario;
        if(this.usuarioActual.getCargo().equalsIgnoreCase("Miembro"))
            usuario="miembro";
        else
            usuario="miembrogipar";
        
        ConexionMYSQL mysql = new ConexionMYSQL(hostname,"bdgipar",usuario);
        Connection conexion = mysql.conectar();
        String sentenciaSQL = "";
        
        
        sentenciaSQL = "DELETE "
                     + "FROM reuniones "
                     + "WHERE idReunion='"+cboReunion.getSelectedItem()+"';";
        
        try {
            Statement ST = conexion.createStatement();
            ST = conexion.createStatement();
            ST.executeUpdate(sentenciaSQL);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return false;
        }
        
        JOptionPane.showMessageDialog(this, "Runion eliminada satisfactoriamente");
        return true;
        
    }
    
    
    
    
