    public boolean agregarPuntoFijo(){
    
        String usuario;
        if(this.usuarioActual.getCargo().equalsIgnoreCase("Miembro"))
            usuario="miembro";
        else
            usuario="miembrogipar";
        
        String nombre,descripcion;
        ConexionMYSQL mysql = new ConexionMYSQL(hostname,"bdgipar",usuario);
        Connection conexion = mysql.conectar();
        String sentenciaSQL = "";
        
        if(txtNombrePuntoFijo.getText().length()==0 || txtDescripcionPuntoFijo.getText().length()==0)
            return false;
        
        sentenciaSQL = "INSERT INTO puntos_fijos(idReunion,nombre,descripcion) VALUES(?, ?, ?)";
        
        PreparedStatement PST;
        try {
            PST = conexion.prepareStatement(sentenciaSQL);
            PST.setString(1, (String) cboReunion.getSelectedItem());
            PST.setString(2, txtNombrePuntoFijo.getText());
            PST.setString(3, txtDescripcionPuntoFijo.getText());
            
            if(PST.executeUpdate()<=0){
                JOptionPane.showMessageDialog(this, "Ha ocurrido un ERROR a la hora de guardar los puntos");
                return false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return false;
        }
        
        JOptionPane.showMessageDialog(this, "Punto fijo agregado satisfactoriamente");
        return true;
    }
    
    public boolean agregarPuntoVario(){
    
        String usuario;
        if(this.usuarioActual.getCargo().equalsIgnoreCase("Miembro"))
            usuario="miembro";
        else
            usuario="miembrogipar";
        
        String idResponsable="";
        ConexionMYSQL mysql = new ConexionMYSQL(hostname,"bdgipar",usuario);
        Connection conexion = mysql.conectar();
        String sentenciaSQL = "";
        
        if(txtNombrePuntoVario.getText().length()==0 || txtDescripcionPuntoVario.getText().length()==0)
            return false;
        
        sentenciaSQL = "SELECT idMiembro FROM miembros WHERE cedula='"+cboResponsable.getSelectedItem()+"'";
        Statement ST;
        try {
            ST = conexion.createStatement();
            ResultSet RS = ST.executeQuery(sentenciaSQL);
            
            if(RS.next()){
                idResponsable = RS.getString("idMiembro");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        sentenciaSQL = "INSERT INTO puntos_varios(idReunion,nombre,descripcion,idResponsable) VALUES(?, ?, ?, ?)";
        
        PreparedStatement PST;
        try {
            PST = conexion.prepareStatement(sentenciaSQL);
            PST.setString(1, (String) cboReunion.getSelectedItem());
            PST.setString(2, txtNombrePuntoVario.getText());
            PST.setString(3, txtDescripcionPuntoVario.getText());
            PST.setString(4, idResponsable);
            
            if(PST.executeUpdate()<=0){
                JOptionPane.showMessageDialog(this, "Ha ocurrido un ERROR a la hora de guardar el articulo");
                return false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return false;
        }
        
        JOptionPane.showMessageDialog(this, "Punto vario agregado satisfactoriamente");
        return true;
    }
    
